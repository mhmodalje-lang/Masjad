async def seed_stories():
    """إضافة القصص للقاعدة"""
    client = AsyncIOMotorClient(MONGO_URL)
    db = client[DB_NAME]
    
    # حذف القصص القديمة (اختياري)
    # await db.stories.delete_many({})
    
    total_added = 0
    
    for category, stories in REAL_STORIES.items():
        for story_data in stories:
            story = {
                "id": str(uuid4()),
                "author_id": "system",
                "author_name": story_data["author_name"],
                "title": story_data["title"],
                "content": story_data["content"],
                "category": category,
                "media_type": "text",
                "created_at": datetime.utcnow().isoformat(),
                "views_count": story_data["views_count"],
                "likes_count": story_data["likes_count"],
                "comments_count": story_data["comments_count"],
                "approved": True,
                "featured": True,
            }
            
            # تحقق من عدم وجود القصة مسبقاً
            existing = await db.stories.find_one({"title": story["title"]})
            if not existing:
                await db.stories.insert_one(story)
                total_added += 1
                print(f"✅ تمت إضافة: {story['title']}")
            else:
                print(f"⏭️  موجودة مسبقاً: {story['title']}")
    
    print(f"\n🎉 تمت إضافة {total_added} قصة بنجاح!")
    client.close()

if __name__ == "__main__":
    asyncio.run(seed_stories())
