async def seed():
    count = await db.ruqyah_items.count_documents({})
    if count > 0:
        print(f"✅ يوجد بالفعل {count} عنصر رقية في القاعدة")
        return
    
    for item in RUQYAH_ITEMS:
        item["id"] = str(uuid.uuid4())[:8]
        item["enabled"] = True
        await db.ruqyah_items.insert_one(item)
    
    print(f"✅ تم إضافة {len(RUQYAH_ITEMS)} عنصر رقية بنجاح!")

if __name__ == "__main__":
    asyncio.run(seed())
