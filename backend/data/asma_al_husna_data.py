def get_asma_al_husna(locale: str = "ar"):
    """Get the 99 Names of Allah in the requested locale."""
    lang = locale if locale in ["ar", "en", "de", "de-AT", "fr", "tr", "ru", "sv", "nl", "el"] else "ar"
    if lang == "de-AT":
        lang = "de"

    result = []
    for name in ASMA_AL_HUSNA:
        result.append({
            "num": name["num"],
            "ar": name["ar"],
            "transliteration": name["transliteration"],
            "meaning": name["meaning"].get(lang, name["meaning"]["ar"])
        })
    return result
